package com.example.operaciones

class op {

    fun suma(num1: Int, num2: Int): Int{
        var sum = num1 + num2
        return sum
    }

    fun resta(num1: Int, num2: Int): Int{
        var resta = num1 * num2
        return resta
    }

    fun multi(num1: Int, num2: Int): Int{
        var multi = num1 * num2
        return multi
    }

    fun div (num1: Int, num2: Int): Int{
        var div = num1 / num2
        return div
    }
}